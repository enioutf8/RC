let modalDescription = document.querySelector('.modal-description');
let modalFichaTecnica = document.querySelector('.modal-ficha-tecnica')

products.forEach(item => {
    let count = 0;
    const div = document.createElement('div');
    div.className = "item-product";
    div.innerHTML = `
        <div data-bs-toggle="modal" data-bs-target="#staticBackdrop"  class="content-product">
            <div class="area-photo">
                <img src="${item.imgs?.[0] || ''}" />
            </div>
            <div class="area-description-product text-default">
            <div class="title-product"> <h5 class="text-default" >${item?.title}</h5></div>
            ${item?.description}
            </div>
        </div>
          <div class="ball-back"></div>
       <div data-bs-toggle="modal" data-bs-target="#staticBackdrop" class="btn-vermais"><button  class="btn btn-lg text-default bg-default">Ver mais</button></div>
    `;
    carroselProduts.appendChild(div);

    //modal
    const btnVermais = div.querySelector('.btn-vermais');
    const contentDiv = div.querySelector('.content-product');
    contentDiv.addEventListener('click', () => handleDataModal(item))
    btnVermais.addEventListener('click', () => handleDataModal(item))
    //modal

    const img = div.querySelector('img');
    const cycleImages = () => {
        count = (count + 1) % item?.imgs?.length;
        img.src = item.imgs[count];
        img.classList.remove('fade-in');
        void img.offsetWidth;
        img.classList.add('fade-in');
        setTimeout(cycleImages, 9000);
    };
    setTimeout(cycleImages, 9000);
});

const handleDataModal = (e) => {
    areaThumbnail.innerHTML = "";
    titleModal.textContent = e.title;
    modalFichaTecnica.innerHTML = e.tecnicalFixa
    modalDescription.innerHTML = `
  
    <div><h5>Descrição: </h5>
    ${e.description}
    </div>
 
    `;
    areaFoto.innerHTML = `<img src="${e.imgs?.[0]}" />`
    e.imgs.forEach((img) => {
        const div = document.createElement('div');
        div.className = "item-thumbail";
        div.innerHTML = `<img src="${img}" />`;
        div.addEventListener('mouseover', () => { hoverImg(img) });
        areaThumbnail.appendChild(div);
    });
};

const hoverImg = (img) => {
    areaFoto.innerHTML = `<img src="${img}" />`
};



